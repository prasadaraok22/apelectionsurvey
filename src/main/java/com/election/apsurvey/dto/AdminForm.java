package com.election.apsurvey.dto;

public class AdminForm {
	
	private String partyResults;
	private String acResults;
	private String emailID;
	
	public String getPartyResults() {
		return partyResults;
	}
	public void setPartyResults(String partyResults) {
		this.partyResults = partyResults;
	}
	public String getAcResults() {
		return acResults;
	}
	public void setAcResults(String acResults) {
		this.acResults = acResults;
	}
	public String getEmailID() {
		return emailID;
	}
	public void setEmailID(String emailID) {
		this.emailID = emailID;
	}
	
}
